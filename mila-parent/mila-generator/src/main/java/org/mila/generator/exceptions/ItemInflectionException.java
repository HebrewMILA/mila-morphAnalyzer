package org.mila.generator.exceptions;

public class ItemInflectionException extends Exception {
	private static final long serialVersionUID = 1L;

	public ItemInflectionException() {
		super();
	}

	public ItemInflectionException(String message, Throwable cause) {
		super(message, cause);
	}

	public ItemInflectionException(String message) {
		super(message);
	}

	public ItemInflectionException(Throwable cause) {
		super(cause);
	}
}
